import { readFile, stat, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const PUBLIC_DIR = resolve(__dirname, '../public')
const SRC_LIB_DIR = resolve(__dirname, '../src/lib')
const ROUTES_DIR = resolve(__dirname, '../src/routes')
const CORE_PKG_PATH = resolve(__dirname, '../../../packages/core/package.json')

const SITE_ORIGIN = (process.env.VITE_SITE_ORIGIN ?? 'https://permx.nuvaynlabs.com').replace(/\/$/, '')
const BASE_PATH = (process.env.PERMX_BASE ?? '/').replace(/\/?$/, '/')
const SITE_URL = `${SITE_ORIGIN}${BASE_PATH}`
const FALLBACK_LASTMOD = new Date().toISOString().slice(0, 10)

// Framework (TanStack Start on CF Workers) 307-redirects trailing-slash
// URLs to non-trailing form. Canonical must match the served URL — so
// sub-pages use non-trailing-slash form. Root (homepage) keeps its slash.
const PAGES = [
  { path: '', priority: '1.0', changefreq: 'weekly', routeFile: 'index.tsx' },
  { path: 'docs', priority: '0.7', changefreq: 'monthly', routeFile: 'docs.index.tsx' },
  { path: 'docs/getting-started', priority: '0.9', changefreq: 'monthly', routeFile: 'docs.getting-started.tsx' },
  { path: 'vs', priority: '0.7', changefreq: 'monthly', routeFile: 'vs.index.tsx' },
  { path: 'vs/casl', priority: '0.8', changefreq: 'monthly', routeFile: 'vs.casl.tsx' },
  { path: 'vs/casbin', priority: '0.8', changefreq: 'monthly', routeFile: 'vs.casbin.tsx' },
  { path: 'vs/permit', priority: '0.8', changefreq: 'monthly', routeFile: 'vs.permit.tsx' },
]

async function routeMtime(routeFile) {
  try {
    const s = await stat(resolve(ROUTES_DIR, routeFile))
    return s.mtime.toISOString().slice(0, 10)
  } catch {
    return FALLBACK_LASTMOD
  }
}

const corePkg = JSON.parse(await readFile(CORE_PKG_PATH, 'utf8'))
const CORE_VERSION = corePkg.version

const pagesWithMtime = await Promise.all(
  PAGES.map(async (p) => ({ ...p, lastmod: await routeMtime(p.routeFile) })),
)

const sitemap = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${pagesWithMtime
  .map(
    (p) => `  <url>
    <loc>${SITE_URL}${p.path}</loc>
    <lastmod>${p.lastmod}</lastmod>
    <changefreq>${p.changefreq}</changefreq>
    <priority>${p.priority}</priority>
  </url>`,
  )
  .join('\n')}
</urlset>
`

const robots = `User-agent: *
Allow: /

Sitemap: ${SITE_URL}sitemap.xml
`

const versionTs = `// Generated by scripts/gen-seo-assets.mjs — do not edit by hand.
// Source of truth: packages/core/package.json version field.
export const PERMX_CORE_VERSION = '${CORE_VERSION}'
`

await writeFile(resolve(PUBLIC_DIR, 'sitemap.xml'), sitemap, 'utf8')
await writeFile(resolve(PUBLIC_DIR, 'robots.txt'), robots, 'utf8')
await writeFile(resolve(SRC_LIB_DIR, 'version.ts'), versionTs, 'utf8')

console.log(
  `[gen-seo-assets] wrote sitemap.xml (${pagesWithMtime.length} urls) + robots.txt + version.ts (${CORE_VERSION}) for ${SITE_URL}`,
)
